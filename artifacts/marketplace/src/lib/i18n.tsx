export type Translations = typeof translations.de;

interface LangContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
}

const LangContext = createContext<LangContextValue>({ lang: "de", setLang: () => {} });

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    try {
      const stored = localStorage.getItem("kb_lang");
      if (stored === "de" || stored === "en") return stored;
    } catch {}
    return "de";
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem("kb_lang", l); } catch {}
  };

  return <LangContext.Provider value={{ lang, setLang }}>{children}</LangContext.Provider>;
}

export function useLanguage() {
  return useContext(LangContext);
}

export function useT(): Translations {
  const { lang } = useLanguage();
  return translations[lang] as Translations;
}

export function getCatLabel(categoryId: string, t: Translations): string {
  const key = `cat_${categoryId.replace(/-/g, "_")}` as keyof Translations;
  return (t[key] as string | undefined) ?? categoryId;
}
